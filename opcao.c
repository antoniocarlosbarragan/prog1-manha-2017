#include <stdio.h>

// exemplo de comando switch com utilização do break

int main()
{
int num;
printf("\nEntre com um numero de 1 a 3:");
scanf("%d", &num);

if(num==1)
printf("\nOpção 1 escolhida!");

else if(num==2)
printf("\nOpção 2 escolhida!");

else if(num==3)
printf("\nOpção 3 escolhida!");

else;
printf("\nOpção inválida");

return 0;
}
